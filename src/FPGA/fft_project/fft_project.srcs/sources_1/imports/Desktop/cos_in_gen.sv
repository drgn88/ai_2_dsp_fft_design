module cos_in_gen (
    input clk,
    input rstn,

    output logic signed [8:0] dout_R[15:0],
    output logic signed [8:0] dout_Q[15:0],
    output logic valid
);

    logic [8:0] w_addr;

    cos_cu #(
        .ADDR_WIDTH(9),
        .OFFSET(16)
    ) U_COS_CU (
        .clk (clk),
        .rstn(rstn),

        .addr (w_addr),
        .valid(valid)
    );

    cos_rom #(
        .OFFSET(16)
    ) U_COS_ROM (
        .en  (valid),
        .addr(w_addr),

        .dout_R(dout_R),
        .dout_Q(dout_Q)
    );

endmodule


module cos_cu #(
    parameter ADDR_WIDTH = 9,
    parameter OFFSET = 16
) (
    input clk,
    input rstn,

    output logic [ADDR_WIDTH-1:0] addr,
    output logic valid
);

    logic [5:0] w_cnt_ctrl;

    cnt_6b U_CNT_6B (
        .clk (clk),
        .rstn(rstn),

        .cnt_ctrl(w_cnt_ctrl)
    );

    logic [ADDR_WIDTH-1:0] addr_reg;

    always_ff @(posedge clk or negedge rstn) begin : valid_ctrl
        if (!rstn) begin
            valid <= 0;
            addr_reg <= 0;
        end else begin
            if (w_cnt_ctrl < 32) begin
                valid <= 1;
                addr_reg <= addr_reg + OFFSET;
            end else begin
                valid <= 0;
                addr_reg <= 0;
            end
        end
    end
    
    //addr 1clk 미루기 위함
    always @(posedge clk or negedge rstn) begin
        if (!rstn) begin
            addr <= 0;
        end else begin
            addr <= addr_reg;
        end
    end

endmodule


module cnt_6b (
    input clk,
    input rstn,

    output logic [5:0] cnt_ctrl
);

    always_ff @(posedge clk or negedge rstn) begin : cnt_6b
        if (!rstn) begin
            cnt_ctrl <= 0;
        end else if (cnt_ctrl == 39) begin
            cnt_ctrl <= 0;
        end else begin
            cnt_ctrl <= cnt_ctrl + 1;
        end
    end

endmodule


